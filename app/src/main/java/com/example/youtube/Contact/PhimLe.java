package com.example.youtube.Contact;

public class PhimLe {
    String img;
    String title;
    String mp4;

    public PhimLe(String img, String title, String mp4) {
        this.img = img;
        this.title = title;
        this.mp4 = mp4;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getMp4() {
        return mp4;
    }

    public void setMp4(String mp4) {
        this.mp4 = mp4;
    }
}
